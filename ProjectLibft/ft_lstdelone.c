#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (!lst)
		return ;
	if (del)
		del(lst->content);
	free(lst);
}

/*lst: Free edilecek eleman.
del: İçeriği silmek için kullanılacak fonksiyonun
adresi.*/